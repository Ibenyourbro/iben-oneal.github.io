function drawChessboard(size) {
  //create an empty string
  let board = "";
  for(let line = 0; line < size; line++){
    for(let character = 0; character < size; character++){
      if((line + character) % 2 === 0){
        board += " ";        
      }else {
        board += "#";
      }
    }
    board += '\n';
  }
  console.log(board)
  }



